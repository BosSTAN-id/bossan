/**
 * RefRek5Search represents the model behind the search form about `app\models\RefRek5`.
 */
class RefRek5Search extends RefRek5
{
    /**
     * @inheritdoc
     */
    public function rules()
    {
        return [
            [['Kd_Rek_1', 'Kd_Rek_2', 'Kd_Rek_3', 'Kd_Rek_4', 'Kd_Rek_5', 'Sekolah'], 'integer'],
            [['Nm_Rek_5'], 'safe'],
        ];
    }

    /**
     * @inheritdoc
